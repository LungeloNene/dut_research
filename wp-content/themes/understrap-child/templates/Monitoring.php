                                    The SMC will comprise, at minimum, a member of the IREC and at least 2 additional
                                    people who meet the following criteria:<br />
                                <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                data-appear-animation-delay="600">
                                    <li>
                                        Suitable expertise and experience in the field of study to be reviewed.
                                    </li>
                                    <li>
                                        Neutrality with regard to the site, to the principal investigator, and to
                                        other relevant parties involved.
                                    </li>
                                </ul></p>
                                <p class="appear-animation" data-appear-animation="fadeInUpShorter"
