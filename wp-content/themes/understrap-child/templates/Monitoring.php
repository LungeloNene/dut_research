                                    <strong>Monitoring procedure</strong><br>Safety monitoring reports:<br>

                                    The principal investigator will submit a safety monitoring and recertification
                                    report form every two years (appendix F), along with any other supporting
                                    documentation, to the IREC, a minimum of three months before ethical approval of the
                                    study lapses. In situations where the IREC deems fit, additional safety monitoring
                                    reports may be requested. Such situations may include<br />
                                <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                    data-appear-animation-delay="600">
                                    <li>
                                        Studies involving vulnerable population groups.
                                    </li>
                                    <li>
                                        Studies graded as Level 3 (possible risk/ risk to humans, animals,
                                        environment or sensitive/ highly sensitive research).
                                    </li>
                                    <li>Studies in which additional factors warrant more stringent monitoring e.g.
                                        sample size, complexity of design, location and number of trial sites,
                                        degree of financial outlay, number of investigators, degree of experience of
                                        the site and staff, degree of manpower involved OR other factors deemed to
                                        justify such additional monitoring by the IREC.</li>
                                    <li>Studies with sites from which complaints have been received.</li>
                                    <li>Studies suspected to be not complying with approved protocol.</li>
                                </ul></p>
                                <p class="appear-animation" data-appear-animation="fadeInUpShorter"
                                data-appear-animation-delay="600">In the absence of suitable expertise necessary, the IREC may source such
                                expertise from other academic institutions or from industry.
                                </p>
