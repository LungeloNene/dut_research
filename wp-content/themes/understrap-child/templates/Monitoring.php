                                    The IREC will monitor compliance with respect to the approved research protocol
                                    ensuring the protection of the research participants. Such continued monitoring
                                    allows for early intervention should proceedings deviate intentionally or
                                    unintentionally from the approved sequence of events<br />

                                    A Safety Monitoring Committee (SMC) will be formulated by the IREC and function as a
                                    sub-committee thereof. The role of the SMC is to perform the following functions:
                                <ol class="appear-animation" data-appear-animation="fadeInUpShorter"
                                    data-appear-animation-delay="600">
                                    <li>
                                        Investigate and report on the following:
                                        <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                            data-appear-animation-delay="600">
                                            <li>Ascertain if an approved study is being conducted according to its
                                                approved protocol.</li>
                                            <li>Ascertain whether an approved study is being conducted according to
                                                the conditions of approval by the IREC.</li>
                                            <li>Ascertain whether amendments to the original protocol are necessary.
                                            </li>
                                        </ul>
                                    </li>
                                    <li>Monitor and report on the following
                                        <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                            data-appear-animation-delay="600">
                                            <li>Progress made in an approved study in respect of the anticipated
                                                timeframe as indicated in the proposal.</li>
                                            <li>Outcomes and findings of such approved studies upon completion
                                                thereof.</li>
                                        </ul>
                                    </li>
                                    <li>
                                        Review and investigate if required:
                                        <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                            data-appear-animation-delay="600">
                                            <li>All adverse events should they arise and advise the IREC
                                                accordingly.</li>
                                            <li>Report to the IREC the findings of the SMC with respect to each
                                                adverse event.</li>
                                        </ul>
                                    </li>
                                </ol>
                                </p>
