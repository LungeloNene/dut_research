                                    In order to perform the site inspection, the SMC may request progress reports from
                                    the principal investigator; such reports if necessary, may be required to be
                                    substantiated by the submission of additional evidence or by undertaking site
                                    inspections. Studies which do not meet the anticipated progress targets set in the
                                    approved protocol will be reported to the IREC for review and intervention.
                                </p>
