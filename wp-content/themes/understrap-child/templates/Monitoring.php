                                    <li>Studies/sites from which serious adverse events or serious adverse drug
                                        reactions are reported.</li>
                                    <li>Studies/sites suspected of committing fraudulent acts.</li>
                                    <li>Studies/sites suspected serious of breach of confidentiality and or poor
                                        handling of participants.</li>
                                    <li>Any additional situations in which the IREC deems an independent audit
                                        necessary.</li>
                                </ul></p>
