                                    In order to perform progress monitoring, all Adverse Event Reports submitted to the
                                    IREC will be delegated to the SMC for investigation, corroboration and reporting.
                                    The SMC may request additional supporting evidence and documentation from the
                                    principal investigator, conduct an appropriate site inspection, or interview
                                    involved parties and stakeholders should it be warranted. Upon concluding its
                                    investigation, a report of the findings will be submitted to the IREC for review.
                                </p>
