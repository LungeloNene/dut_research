                                    following:<br />
                                <ul class="appear-animation" data-appear-animation="fadeInUpShorter"
                                    data-appear-animation-delay="600">
                                    <li>
                                        The presence and suitability of all trial documentation and essential
                                        documents (appendix F).
                                    </li>
                                    <li>
                                        Appropriateness and suitability of facilities and infrastructure at the
                                        trial site.
                                    </li>
                                    <li>Suitability of expertise and staff recruited to participate in or facilitate
                                        the research process</li>
                                    <li>Investigational equipment for monitoring and interventions made.</li>
                                    <li>Investigational products and interventions with respect to storage,
                                        labelling, dispensing, counselling of participants, administration, stock
                                        control, and disposal thereof</li>
                                    <li>Administration and storage of all trial documentation.</li>
                                    <li>Evidence of recruitment strategy and practice applied (including informed
                                        consent).</li>
                                    <li>General evidence to support the degree of compliance with the approved
                                        research protocol.</li>
                                </ul>All studies categorised as Level 3 as defined in the proposal document, will be
